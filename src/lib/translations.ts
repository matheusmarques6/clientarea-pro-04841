export const getTranslation = (type: 'returns' | 'refunds', language: string, key: string): any => {
  const lang = supportedLanguages.find(l => l.code === language)?.code || 'pt';
  const translation = translations[type][lang as keyof typeof translations.returns];
  
  // Navigate through nested keys (e.g., 'methods.card')
  const keys = key.split('.');
  let result: any = translation;
  
  for (const k of keys) {
    if (result && typeof result === 'object' && k in result) {
      result = result[k];
    } else {
      // Fallback to Portuguese if key not found
      result = translations[type].pt;
      for (const fallbackKey of keys) {
        if (result && typeof result === 'object' && fallbackKey in result) {
          result = result[fallbackKey];
        } else {
          return key; // Return the key itself if not found
        }
      }
      break;
    }
  }
  
  return result || key;
};
